    public void setaentra() throws SQLException{
        Double valorent=0.0;
        Double valorsai=0.0;
        String oi=(String) jComboBox1.getSelectedItem();
        String[] separa = oi.split(":");
        Comvenda comvenda = new Comvenda();
        for (Venda p :comvenda.retornandovalores(Integer.parseInt(separa[1]))) {
           valorent=(p.getTot_entrada())+valorent;
           valorsai=(p.getTot_saida())+valorsai;
           
        }
        jTextField2.setText("R$ "+String.valueOf(valorent));
        jTextField1.setText("R$ "+String.valueOf(valorent));
        jTextField6.setText("R$ "+String.valueOf(valorsai));
        jTextField8.setText("R$ "+String.valueOf(valorsai));
        
        
    }

