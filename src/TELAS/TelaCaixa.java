    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realizar esta ação ?");
                    if (resposta == JOptionPane.YES_OPTION) {
                        
        String oi=(String) jComboBox1.getSelectedItem();
    String[] separa = oi.split(":");
        try {
            //    JOptionPane.showMessageDialog(null, separa[1]);
            Comcaixa comcaixa = new Comcaixa();         //alterando situacao do caixa
            for (Caixa caixa : comcaixa.alteradinheirocaixa(Integer.parseInt(separa[1]))) {
            caixa.setId(Integer.parseInt(separa[1]));
                if (caixa.getSituacao()) {
                   caixa.setSituacao(false);
                }else{
                   caixa.setSituacao(true);
                    
                }
            comcaixa.alterasositu(caixa);
            this.verifica();
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaCaixa.class.getName()).log(Level.SEVERE, null, ex);
        }
                    }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

