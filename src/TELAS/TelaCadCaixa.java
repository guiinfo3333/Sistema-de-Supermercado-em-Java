    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            if(q.getText()==null || f.getText().trim().equals("") ){
                JOptionPane.showMessageDialog(null,"Preencha os espaços vazios !!");
            }else{
                Comcaixa ver = new Comcaixa();
                if (!ver.verificacaixa(Integer.valueOf(f.getText()))) {
                    
                    int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente cadastrar este caixa?");
                    if (resposta == JOptionPane.YES_OPTION) {
                        Caixa p = new Caixa ();
                        p.setDinheiro(Double.parseDouble((q.getText())));
                        p.setIdfuncionario(Integer.parseInt((f.getText())));
                        p.setSituacao(false);
                      

                        try {
                            Comcaixa cp = new Comcaixa();
                            cp.adiciona(p);
                        } catch (SQLException ex) {
                            Logger.getLogger(TelaCadCaixa.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                }//fim do else para saber se o produto já está cadastrado
else{
                    JOptionPane.showMessageDialog(null,"Este caixa já foi cadastrado!!");
                }
            }
            readJTable();
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadCaixa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

