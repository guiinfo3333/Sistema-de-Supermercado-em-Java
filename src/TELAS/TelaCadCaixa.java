    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente alterar este caixa ?");
        if (resposta == JOptionPane.YES_OPTION) {
            Caixa p = new Caixa();
            Comcaixa dao;
            p.setDinheiro(Double.parseDouble((q.getText())));
            p.setIdfuncionario(Integer.parseInt((f.getText())));
            p.setId((int) tabela.getValueAt(tabela.getSelectedRow(), 0));  //pegamos o id da linha selecionada
            try {
                dao = new Comcaixa();
                dao.altera(p);
                readJTable();
            } catch (SQLException ex) {
                Logger.getLogger(TelaCadCaixa.class.getName()).log(Level.SEVERE, null, ex);
            }
            q.setText("");
            f.setText("");
          

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (tabela.getSelectedRow() != -1) {
            int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente apagar este caixa?");
            if (resposta == JOptionPane.YES_OPTION) {   //if para confirmacao

                Caixa p = new Caixa();
                p.setId((int) tabela.getValueAt(tabela.getSelectedRow(), 0));  //botando para dentro do array o valor do id
                Comcaixa dao;
                try {
                    dao = new Comcaixa();
                    dao.remove(p);
                } catch (SQLException ex) {
                    Logger.getLogger(TelaCadCaixa.class.getName()).log(Level.SEVERE, null, ex);
                }

                q.setText("");
                f.setText("");
                try {
                    readJTable();  //para ler novamente para atualizar
                } catch (SQLException ex) {
                    Logger.getLogger(Comcaixa.class.getName()).log(Level.SEVERE, null, ex);
                }

            }} else {
                JOptionPane.showMessageDialog(null, "Selecione um caixa para excluir");
            }
    }//GEN-LAST:event_jButton5ActionPerformed

