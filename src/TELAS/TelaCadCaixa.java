       public void readJTable() throws SQLException {
        
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setNumRows(0); //para tirar todas para nao dá o problema da duplicação
           Comcaixa prod = new Comcaixa();

        for (Caixa p : prod.getLista ()) {  //ele vai pegar o que tiver dentro do array list e colocar
            //dentro do objeto do produto p
           String ntexto;
           if ( p.getSituacao()) {
                ntexto="aberto";
           }else{
                ntexto="fechado";
               
           }
            modelo.addRow(new Object[]{    //criamos um vetor que vai pegar o que já tem
                p.getId(),
                p.getNomefunc(),
                p.getDinheiro(),
                ntexto
            });
        }

    }

