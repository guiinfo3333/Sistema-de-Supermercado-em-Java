    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (chave=="func") {
            try {
                Comfuncionario comfunc = new Comfuncionario();
//                Funcionario f = new Funcionario();
                if(comfunc.login(txtLogin.getText(),txtsenha.getText())){
                      new Thread(){
              @Override
              public void run(){
                  for (int i=0;i<101;i++){
                      jProgressBar.setValue(i);
                   try{
                       Thread.sleep(35);
                   }catch(Exception ex){
                       ex.getMessage();
                   }
                  }
                  try {
                      for (Funcionario p : comfunc.pegaid(txtLogin.getText())) {
//                          JOptionPane.showMessageDialog(null,p.getId());
                     PDV pdv = new PDV(p.getId());
                    pdv.setVisible(true);
                    pdv.setIdfuncionario(p.getId());
                    dispose();
                     tela.setVisible(false);
                      }
                  } catch (SQLException ex) {
                      Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
                  }
              }
          }.start(); 
                }else{
                   JOptionPane.showMessageDialog(null,"login ou senha incorretos!");  
                }
            } catch (SQLException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
             try {
                 Comadministrador comadmin = new Comadministrador();
//                Funcionario f = new Funcionario();
                if(comadmin.login(txtLogin.getText(),txtsenha.getText())){
                      new Thread(){
              @Override
              public void run(){
                  for (int i=0;i<101;i++){
                      jProgressBar.setValue(i);
                   try{
                       Thread.sleep(35);
                   }catch(Exception ex){
                       ex.getMessage();
                   }
                  }
                  
                  try {
                      new TelaInicial(txtLogin.getText()).setVisible(true);
                  } catch (SQLException ex) {
                      Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
                  }
                  dispose();
                  tela.setVisible(false);
              }
          }.start(); 
                }else{
                   JOptionPane.showMessageDialog(null,"login ou senha incorretos!");  
                }
            } catch (SQLException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed
    }


